// Build-time manifest generator for serverless deployments.
//
// Generates .manta/server/manifest.ts with:
//  1. Static imports for every user file (models, services, commands, queries, links, etc.)
//  2. A pre-computed DiscoveredResources object (no readdirSync at runtime)
//  3. An import lookup map (no jiti at runtime)
//
// This is the fix for the fundamental serverless incompatibility: bootstrapApp's
// runtime filesystem discovery (readdirSync + jiti.import) doesn't work on Vercel
// because the source files aren't on the function's filesystem as individual files —
// they need to be statically imported and bundled by Nitro/rolldown.

import { existsSync, mkdirSync, writeFileSync } from 'node:fs'
import { basename, dirname, join, relative, resolve } from 'node:path'
import type { DiscoveredResources } from '../resource-loader'

/**
 * Collect all file paths that bootstrapApp needs to import from a DiscoveredResources object.
 */
function collectImportPaths(resources: DiscoveredResources, pluginResources: Array<{ name: string; resources: DiscoveredResources; rootDir: string }>): Array<{ absPath: string; label: string }> {
  const paths: Array<{ absPath: string; label: string }> = []

  function addFromResources(r: DiscoveredResources, prefix: string) {
    for (const m of r.modules) {
      for (const e of m.entities) {
        paths.push({ absPath: e.modelPath, label: `${prefix}model_${m.name}_${e.name}` })
        if (e.servicePath) paths.push({ absPath: e.servicePath, label: `${prefix}svc_${m.name}_${e.name}` })
      }
      for (const c of m.commands) paths.push({ absPath: c.path, label: `${prefix}mcmd_${m.name}_${c.id}` })
      for (const q of m.queries) paths.push({ absPath: q.path, label: `${prefix}mquery_${m.name}_${q.id}` })
      for (const l of m.intraLinks) paths.push({ absPath: l.path, label: `${prefix}mlink_${m.name}_${l.id}` })
      for (const a of m.apiRoutes) paths.push({ absPath: a.file, label: `${prefix}mapi_${m.name}_${a.relativePath.replace(/[^a-z0-9]/gi, '_')}` })
    }
    for (const c of r.commands) paths.push({ absPath: c.path, label: `${prefix}cmd_${c.id}` })
    for (const q of r.queries) paths.push({ absPath: q.path, label: `${prefix}query_${q.id}` })
    for (const l of r.links) paths.push({ absPath: l.path, label: `${prefix}link_${l.id}` })
    for (const s of r.subscribers) paths.push({ absPath: s.path, label: `${prefix}sub_${s.id}` })
    for (const w of r.workflows) paths.push({ absPath: w.path, label: `${prefix}wf_${w.id}` })
    for (const j of r.jobs) paths.push({ absPath: j.path, label: `${prefix}job_${j.id}` })
    for (const a of r.agents) paths.push({ absPath: a.path, label: `${prefix}agent_${a.id}` })
    for (const u of r.users) paths.push({ absPath: u.path, label: `${prefix}user_${u.contextName}` })
    for (const cm of r.contextMiddlewares) paths.push({ absPath: cm.path, label: `${prefix}mw_${cm.context}` })
    if (r.middlewares) paths.push({ absPath: r.middlewares.path, label: `${prefix}middlewares` })
  }

  addFromResources(resources, '')
  for (const p of pluginResources) {
    addFromResources(p.resources, `plugin_${p.name.replace(/[^a-z0-9]/gi, '_')}_`)
  }

  return paths
}

/**
 * Sanitize a label to be a valid JS identifier.
 */
function sanitize(s: string): string {
  return s.replace(/[^a-zA-Z0-9_]/g, '_').replace(/^(\d)/, '_$1')
}

/**
 * Generate .manta/server/manifest.ts with static imports for all user/plugin files.
 */
export function generateBuildManifest(
  cwd: string,
  resources: DiscoveredResources,
  pluginResources: Array<{ name: string; resources: DiscoveredResources; rootDir: string }>,
): void {
  const targetDir = resolve(cwd, '.manta', 'server')
  if (!existsSync(targetDir)) mkdirSync(targetDir, { recursive: true })

  const allPaths = collectImportPaths(resources, pluginResources)

  // Deduplicate by absPath
  const seen = new Set<string>()
  const uniquePaths = allPaths.filter(p => {
    if (seen.has(p.absPath)) return false
    seen.add(p.absPath)
    return true
  })

  const lines: string[] = [
    '// Auto-generated by manta build — DO NOT EDIT',
    '// Static imports for serverless: all user modules, commands, queries, etc.',
    '// Bundled by Nitro/rolldown at build time → no filesystem access at runtime.',
    '',
  ]

  // Generate import statements with relative paths from .manta/server/ to each file
  for (let i = 0; i < uniquePaths.length; i++) {
    const { absPath } = uniquePaths[i]
    let relPath = relative(targetDir, absPath)
    // Remove .ts extension for import (bundler resolves it)
    relPath = relPath.replace(/\.tsx?$/, '')
    // Ensure relative path starts with ./
    if (!relPath.startsWith('.')) relPath = './' + relPath
    lines.push(`import * as _i${i} from '${relPath}'`)
  }

  lines.push('')
  lines.push('// Import lookup: absolute path (at build time) → pre-imported module.')
  lines.push('// bootstrapApp\'s importFn uses this map instead of jiti.import().')
  lines.push('export const moduleExports: Record<string, Record<string, unknown>> = {')
  for (let i = 0; i < uniquePaths.length; i++) {
    const { absPath } = uniquePaths[i]
    lines.push(`  ${JSON.stringify(absPath)}: _i${i} as unknown as Record<string, unknown>,`)
  }
  lines.push('}')

  lines.push('')
  lines.push('// Pre-discovered resources structure (serialized from discoverResources at build time).')
  lines.push('// bootstrapApp uses this instead of scanning the filesystem with readdirSync.')
  lines.push(`export const preloadedResources = ${JSON.stringify(resources, null, 2)} as const`)

  // Plugin resources
  lines.push('')
  lines.push(`export const preloadedPluginResources = ${JSON.stringify(pluginResources.map(p => ({ name: p.name, resources: p.resources, rootDir: p.rootDir })), null, 2)} as const`)

  lines.push('')

  const content = lines.join('\n')
  writeFileSync(resolve(targetDir, 'manifest.ts'), content)

  // Generate a tsconfig.json in .manta/server/ so rolldown can resolve the TS imports
  // in the manifest. Without this, imports that traverse to packages/ (plugin files)
  // fail with "Tsconfig not found" because rolldown walks up from .manta/server/ and
  // doesn't find a tsconfig before hitting the root.
  const tsconfig = {
    compilerOptions: {
      target: 'ES2022',
      module: 'ESNext',
      moduleResolution: 'bundler',
      strict: false,
      esModuleInterop: true,
      skipLibCheck: true,
      allowJs: true,
      jsx: 'react-jsx',
    },
    include: ['**/*.ts', '**/*.tsx'],
  }
  writeFileSync(resolve(targetDir, 'tsconfig.json'), JSON.stringify(tsconfig, null, 2))

  console.log(`  ✓ Build manifest generated (${uniquePaths.length} static imports)`)
}
