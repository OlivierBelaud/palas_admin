// ── DML entity type guard ─────────────────────────────────────────

export interface DmlEntityLike {
  name: string
  schema: Record<string, unknown>
  getOptions?: () => Record<string, unknown>
}

export function isDmlEntity(value: unknown): value is DmlEntityLike {
  return (
    typeof value === 'object' &&
    value !== null &&
    'name' in value &&
    'schema' in value &&
    typeof (value as Record<string, unknown>).name === 'string'
  )
}

// ── Service class introspection type guard ────────────────────────

export function hasModelObjects(
  cls: unknown,
): cls is (new (...args: unknown[]) => unknown) & { $modelObjects: Record<string, unknown> } {
  return typeof cls === 'function' && '$modelObjects' in cls
}

// ── Raw SQL with unsafe() ─────────────────────────────────────────

type PostgresSql = ReturnType<typeof postgres>

