/**
 * Generate link and unlink commands for a ResolvedLink.
 *
 * Names are camelCase for ergonomic usage: `step.command.linkCustomerCustomerGroup()`
 *
 * @example
 * ```typescript
 * // From: defineLink(many('customer'), many('customer_group'))
 * // Generates:
 * //   { name: 'linkCustomerCustomerGroup', ... }
 * //   { name: 'unlinkCustomerCustomerGroup', ... }
 * // Usage: step.command.linkCustomerCustomerGroup({ customer_id: '...', customer_group_id: '...' })
 * // HTTP:  POST /api/admin/command/link-customer-customer-group
 * ```
 */
export function generateLinkCommands(link: ResolvedLink): EntityCommand[] {
  const leftCamel = toCamel(link.leftEntity)
  const rightCamel = toCamel(link.rightEntity)
  const leftCap = leftCamel.charAt(0).toUpperCase() + leftCamel.slice(1)
  const rightCap = rightCamel.charAt(0).toUpperCase() + rightCamel.slice(1)
  const leftLower = link.leftEntity.toLowerCase()
  const rightLower = link.rightEntity.toLowerCase()

  const leftFk = link.leftFk
  const rightFk = link.rightFk

  const inputSchema = z.object({
    [leftFk]: z.string(),
    [rightFk]: z.string(),
  })

  const commands: EntityCommand[] = []

  // --- link ---
  commands.push({
    __type: 'command',
    __module: link.leftModule ?? leftLower,
    __entity: `${link.leftEntity}_${link.rightEntity}`,
    __operation: 'link',
    __autoGenerated: true,
    name: `link${leftCap}${rightCap}`,
    description: `Link a ${leftLower} to a ${rightLower}`,
    input: inputSchema,
    workflow: async (input: unknown, ctx: unknown) => {
      const data = input as Record<string, string>
      const leftId = data[leftFk]
      const rightId = data[rightFk]

      // biome-ignore lint/suspicious/noExplicitAny: dynamic app resolution
      const app = (ctx as any).app
      const db = (app.infra as Record<string, unknown>).db as {
        insert: (table: unknown) => { values: (v: unknown) => { onConflictDoNothing: () => Promise<unknown> } }
      }
      const tables = app.resolve('__generatedTables') as Map<string, unknown>
      const linkTable = tables.get(link.tableName)
      if (!linkTable) {
        throw new Error(`Link table "${link.tableName}" not found`)
      }

      await db
        .insert(linkTable)
        .values({
          id: `link_${leftId}_${rightId}`,
          [leftFk]: leftId,
          [rightFk]: rightId,
          created_at: new Date(),
          updated_at: new Date(),
        })
        .onConflictDoNothing()

      const eventBus = app.infra?.eventBus
      if (eventBus) {
        await eventBus.emit(`${leftLower}.${rightLower}.linked`, { [leftFk]: leftId, [rightFk]: rightId })
      }

      return { success: true }
    },
  })

  // --- unlink ---
  commands.push({
    __type: 'command',
    __module: link.leftModule ?? leftLower,
    __entity: `${link.leftEntity}_${link.rightEntity}`,
    __operation: 'unlink',
    __autoGenerated: true,
    name: `unlink${leftCap}${rightCap}`,
    description: `Unlink a ${leftLower} from a ${rightLower}`,
    input: inputSchema,
    workflow: async (input: unknown, ctx: unknown) => {
      const data = input as Record<string, string>
      const leftId = data[leftFk]
      const rightId = data[rightFk]

      // biome-ignore lint/suspicious/noExplicitAny: dynamic app resolution
      const app = (ctx as any).app
      const db = (app.infra as Record<string, unknown>).db as {
        delete: (table: unknown) => { where: (cond: unknown) => Promise<unknown> }
      }
      const tables = app.resolve('__generatedTables') as Map<string, unknown>
      const linkTable = tables.get(link.tableName)
      if (!linkTable) {
        throw new Error(`Link table "${link.tableName}" not found`)
      }

      // biome-ignore lint/suspicious/noExplicitAny: Drizzle dynamic column access
      const leftFkCol = (linkTable as any)[leftFk]
      // biome-ignore lint/suspicious/noExplicitAny: Drizzle dynamic column access
      const rightFkCol = (linkTable as any)[rightFk]

      const { eq, and } = await import('drizzle-orm')
      await db.delete(linkTable).where(and(eq(leftFkCol, leftId), eq(rightFkCol, rightId)))

      const eventBus = app.infra?.eventBus
      if (eventBus) {
        await eventBus.emit(`${leftLower}.${rightLower}.unlinked`, { [leftFk]: leftId, [rightFk]: rightId })
      }

      return { success: true }
    },
  })

  return commands
}
